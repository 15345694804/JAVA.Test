package Yyy;

public class JAVA {
    public static void main(String[] args) {
        int a,b;
        for(a = 1;a<=9;a++){
            for(b = 1;b <=9;b++){
                if(a==5||b==5){
                    System.out.println("*");
                }else {
                    System.out.println(" ");

                }
                System.out.println("\n");
            }
        }
    }
}
