package Array;
/*
    定义一个数组
 */
public class ArrayDemo01 {
    public static void main(String[] args) {
        //定义数组
        int[] arr = {1,2,3};

        //输出数组名
        System.out.println(arr);

        //输出数组中的元素
        System.out.println(arr[0]);
        System.out.println(arr[1]);
        System.out.println(arr[2]);
    }
}
